const MAX_QUESTIONS = 10;

export const Home = () => {
  const [currentQuestion, setCurrentQuestion] = useState<number>(0);
  const [selectedTexts, setSelectedTexts] = useState<TextSelection[]>([]);

  const handleNext = () => {};
  const handlePrev = () => {};

  const handleQuestionChange = (event: ChangeEvent<HTMLInputElement>) => {
    if (currentQuestion < MAX_QUESTIONS) {
      const question = event.target.value;
    }
  };

  const handleTextSelection = (highlightedText: TextSelection) => {
    setSelectedTexts((prev) => [...prev, highlightedText]);
  };

  const handleDeleteTextSelection = (index: number) => {
    const updatedSelectedTexts = produce(selectedTexts, (draft) => {
      draft.splice(index, 1);
      return draft;
    });
    console.log(updatedSelectedTexts);
    setSelectedTexts(updatedSelectedTexts);
  };

  return (
    <Box
      sx={{
        height: "100vh",
        width: "100vw",
        display: "flex",
        flexDirection: "row",
        overflow: "hidden",
      }}
    >
      <Box
        sx={{
          display: "flex",
          flexDirection: "column",
          overflow: "scroll",
          flex: 1,
          boxSizing: "border-box",
          p: 4,
          gap: 4,
        }}
      >
        <Box
          sx={{
            border: (theme) => `0.5px solid ${theme.palette.primary.light}`,
            borderRadius: "8px",
            height: "40%",
          }}
        >
          <TextSelector
            id="main-editor"
            text={dummyText}
            selectedTexts={selectedTexts.filter(
              (item) => item.id === "main-editor"
            )}
            onTextSelection={handleTextSelection}
          />
        </Box>
        <Box
          sx={{
            position: "relative",
            display: "flex",
            flexDirection: "column",
            flex: "1",
            gap: 2,
          }}
        >
          <LabelledInput
            label={<Typography variant="subtitle1">Question</Typography>}
            size="small"
            fullWidth
            type="text"
            InputProps={{
              endAdornment: (
                <IconButton size="small">
                  <Send />
                </IconButton>
              ),
            }}
          />
          <Typography variant="subtitle1">Results</Typography>
          {dummyResults.map((result, index) => {
            const id = "result_" + index;
            const editorSelectedText = selectedTexts.filter(
              (item) => item.id == id
            );
            return (
              <Box
                key={index}
                sx={{
                  display: "flex",
                  overflow: "scroll",
                  flexDirection: "column",
                  border: (theme) =>
                    `0.5px solid ${theme.palette.primary.light}`,
                  borderRadius: "8px",
                  mb: "24px",
                  maxHeight: "340px",
                }}
              >
                <TextSelector
                  id={id}
                  text={result}
                  selectedTexts={editorSelectedText}
                  onTextSelection={handleTextSelection}
                />
              </Box>
            );
          })}
          <LabelledInput
            label={<Typography variant="subtitle1">Additional Info</Typography>}
            size="small"
            fullWidth
            type="text"
          />
          <Box sx={{ display: "flex", justifyContent: "space-between" }}>
            <Button
              variant="contained"
              sx={{ width: "120px" }}
              disabled={currentQuestion === 0}
              onClick={handlePrev}
            >
              Prev
            </Button>
            <Button
              variant="contained"
              sx={{ width: "120px" }}
              disabled={currentQuestion === MAX_QUESTIONS}
              onClick={handleNext}
            >
              Next
            </Button>
          </Box>
        </Box>
      </Box>
      {selectedTexts.length > 0 && (
        <>
          <Divider orientation="vertical" />
          <SelectionSummarySidePanel
            textSelections={selectedTexts}
            onDelete={handleDeleteTextSelection}
            onSelect={() => {}}
          />
        </>
      )}
    </Box>
  );
};
